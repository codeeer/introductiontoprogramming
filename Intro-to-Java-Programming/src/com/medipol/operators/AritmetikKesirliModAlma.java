package com.medipol.operators;

public class AritmetikKesirliModAlma {

	public static void main(String[] args) {
		
		double x = 10.57;// double tipinde x degiskeni tanimladik ona da 10.57 degerini atadik.
		double y = 4.83; // doubke tipinde y degiskeni tanimladik degiskene 4.83 degeri atandi.
		double sonuc = x % y; // x in y ye gore modunu aldik cikan sonucu double tipindeki sonuc degiskenine atadik.
		System.out.println("x % y = " + sonuc);
	}

}
