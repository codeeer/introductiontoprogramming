package com.medipol.operators;

public class AritmetikSayisalTipler {

	public static void main(String[] args) {
		int x = 10;
		float y = 3.0f;
		double z = 3.0;
		
		System.out.println("x + y = " + (x + y));
		System.out.println("x - y = " + (x - y));
		System.out.println("x * y = " + x * y);
		System.out.println("x / y = " + x / y);
		
		System.out.println();
		System.out.println("x + z = " + (x + z));
		System.out.println("x - z = " + (x - z));
		System.out.println("x * z = " + x * z);
		System.out.println("x / z = " + x / z);

	}

}
