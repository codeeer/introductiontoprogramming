package com.medipol.loops;

public class WhileDongusu1 {

	public static void main(String[] args) {
		
		int k = 1;
		
		while (k <= 5) {

		   if(k==5) {
			   System.out.print(k+  " " );
		   }
		   else {
				System.out.print(k + ","); 
		   }
		   k++;
		
		}
		System.out.println("bitti");
	}

}
